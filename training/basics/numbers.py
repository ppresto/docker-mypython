#!/usr/bin/env python3.7
# Ref:  https://docs.python.org/3/library/stdtypes.html#numeric-types-int-float-complex

print (2+2)
print (10-4)
print (3*9)
print (5/3)
print (5//3) # floor division if you want to int 1 instead of 1.6666
print (5%3)  # remainder (2)
print (2 ** 3)  # power of 3.  can also use pow(2,3)
print (pow(2,3))
print (float('7.0'))  # convert strint to float.  Make sure its float for class type.
print (int(5.6))  # int class will truncate
print (str(7.0))  # convert back to string
